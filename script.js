/*
 *
 * returns {array}
 */
function extractArray(obj) {
  let arr = [];
  if (Array.isArray(obj)) {
    for (let elem of obj) {
      arr.push(elem);
    }
    return arr;
  }
  for (let key in obj) {
    let extractedArr = extractArray(obj[key]);
    for (let elem of extractedArr) {
      arr.push(elem);
    }
  }
  return arr;
}

/*
 * Attaches an array of sentences to a root element
 * @param {array}
 * @param {HTMLElement}
 * @returns {void}
 */
function fillRoot(arr, root) {
  root.innerText = '';
  for (let elem of arr) {
    let p = document.createElement('p');
    p.innerText = elem;
    root.appendChild(p);
  }
}

function load(elem, key) {
  for (let el of document.querySelectorAll('div.nav p')) {
    el.style.borderColor = 'transparent';
  }
  elem.style.borderColor = '#888';
  let root = document.getElementById('root');
  let status = document.createElement('p');
  status.innerText = 'Loading...';
  root.appendChild(status);

  let dumkyArray = extractArray(dumkyHash[key]);
  fillRoot(dumkyArray, root);

  status.innerText = '';
}

